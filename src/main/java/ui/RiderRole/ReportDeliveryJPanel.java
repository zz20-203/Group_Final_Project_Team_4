    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        int selectedRow = tblOrderList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an order to accept.");
            return;
        }
        
        CoffeeOrderRequest order = (CoffeeOrderRequest) tblOrderList.getValueAt(selectedRow, 0);
        String status = order.getStatus();
        
        if ("In-Progress".equalsIgnoreCase(status) || "Delivered".equalsIgnoreCase(status)) {
            JOptionPane.showMessageDialog(this, "This order has already been accepted or delivered.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Accept this delivery job?", "Confirm Acceptance", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            order.setStatus("In-Progress");
            
            Delivery d = organization.getDeliveryDirectory().findDeliveryByOrder(order);
            if (d != null) {
                d.setDateTimeSent(System.currentTimeMillis()); 
            }
            
            populateTable();
            JOptionPane.showMessageDialog(this, "Order accepted. Status set to In-Progress.");
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        int selectedRow = tblOrderList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an order to report.");
            return;
        }
        
        CoffeeOrderRequest order = (CoffeeOrderRequest) tblOrderList.getValueAt(selectedRow, 0);
        String status = order.getStatus();
        
        if (!"In-Progress".equalsIgnoreCase(status)) {
            JOptionPane.showMessageDialog(this, "You must Accept the order before reporting it as delivered.");
            return;
        }
        
        // Confirmation 1
        int confirm1 = JOptionPane.showConfirmDialog(this, "Have you arrived at the destination and delivered the items?", "Confirm Delivery - Step 1", JOptionPane.YES_NO_OPTION);
        if (confirm1 == JOptionPane.YES_OPTION) {
            
            // Confirmation 2
            int confirm2 = JOptionPane.showConfirmDialog(this, "Are you sure you want to mark this as Complete? This cannot be undone.", "Final Confirmation - Step 2", JOptionPane.YES_NO_OPTION);
            
            if (confirm2 == JOptionPane.YES_OPTION) {
                order.setStatus("Delivered");
                
                // Update Delivery Time
                Delivery d = organization.getDeliveryDirectory().findDeliveryByOrder(order);
                if (d != null) {
                    d.setDateTimeArrived(System.currentTimeMillis());
                }
                
                populateTable();
                JOptionPane.showMessageDialog(this, "Delivery reported successfully!");
            }
        }
    }//GEN-LAST:event_btnReportActionPerformed


