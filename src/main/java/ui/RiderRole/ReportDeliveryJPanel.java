    private void identifyRider() {
        String empName = userAccount.getEmployee().getName();
        for (Rider r : organization.getRiderDirectory().getEmployeeList()) {
            String riderName = r.getFirstName() + " " + r.getLastName();
            if (riderName.equals(empName)) {
                currentRider = r;
                break;
            }
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrderList.getModel();
        model.setRowCount(0);
        
        if (currentRider == null) return;
        
        for (Delivery d : organization.getDeliveryDirectory().getDeliveryList()) {
            if (d.getRider().equals(currentRider)) {
                CoffeeOrderRequest order = d.getOrder();
                Object[] row = new Object[4];
                row[0] = order;
                row[1] = order.getMessage();
                row[2] = order.getStatus();
                row[3] = (order.getDestination() != null) ? order.getDestination().getAddress() : "N/A";
                
                model.addRow(row);
            }
        }
    }

