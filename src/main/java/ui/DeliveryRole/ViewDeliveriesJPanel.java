    private void btnAssignRiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignRiderActionPerformed
        int selectedRow = tblOrders.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an order from the list.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        CoffeeOrderRequest selectedOrder = (CoffeeOrderRequest) tblOrders.getValueAt(selectedRow, 0);
        
        if ("Delivered".equalsIgnoreCase(selectedOrder.getStatus())) {
            JOptionPane.showMessageDialog(this, "This order has already been delivered and cannot be modified.", "Action Not Allowed", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        AssignRiderJPanel panel = new AssignRiderJPanel(userProcessContainer, orderQueue, riderDirectory, deliveryDirectory, selectedOrder);
        userProcessContainer.add("AssignRiderJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnAssignRiderActionPerformed


