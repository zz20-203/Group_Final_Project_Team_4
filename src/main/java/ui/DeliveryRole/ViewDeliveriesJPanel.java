    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrders.getModel();
        model.setRowCount(0);
        
        for (OrderRequest request : orderQueue.getWorkRequestList()) {
            if (request instanceof CoffeeOrderRequest) {
                CoffeeOrderRequest order = (CoffeeOrderRequest) request;
                
                Delivery delivery = deliveryDirectory.findDeliveryByOrder(order);
                
                Object[] row = new Object[6];
                row[0] = order; 
                row[1] = order.getRequestDate();
                
                if (delivery != null) {
                    row[2] = delivery.getDateTimeArrived() == 0 ? "Pending" : delivery.getDateTimeArrived();
                    row[3] = delivery.getRider().getFirstName() + " " + delivery.getRider().getLastName();
                } else {
                    row[2] = "N/A";
                    row[3] = "Unassigned";
                }
                
                if (order.getDestination() != null) {
                    row[4] = order.getDestination().getAddress();
                } else {
                    row[4] = "In-Store/None";
                }
                
                row[5] = order.getStatus();
                
                model.addRow(row);
            }
        }
    }

